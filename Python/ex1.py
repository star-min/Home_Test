print('Hello python')   # Hello Python 을 출력
# 변수명은 개발자가 정하는 것으로 영문으로 시작해야하며
# 영문 대소문자를 구분하며, 두번째글자부터는 숫자를 사용 가능
var1 = "김성민"   # 변수명 = 기억장소이름
var2 = 1004      # 파이썬은 저장하는 데이터에 따라 변수명이 달라짐
# var1 문자형 변수 var2 정수형변수
print(var1, var2)
var3 = 3.141592 #실수형 변수(소수점이하가 있는 수)
var4 = True # 논리형 변수 True 아니면 False
print(id(var1), id(var2), id(var3), id(var4))
# 변수의 종류 : 문자열 변수, 정수형 변수, 실수형 변수, 논리형 변수