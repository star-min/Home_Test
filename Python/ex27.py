# 튜플의 함수 (type, len) 또는 메서드(count, index)
# 함수 : len(tp1), type(tp1)
# 매서드 : tp1.count(값), tp1.index(값)
tp1 = (80, 90, 95, 70, 85, 75)
print("tp1의 자료형 : ", type(tp1))
print("tp1의 값 : ", tp1)
lst1 = list(tp1)
print("lst1의 자료형 : ", type(lst1))
print("lst1의 값 : ", lst1)
lst1.remove(90)
print("lst1의 요소 제거후 : ", lst1)
tp1 = tuple(lst1)
print("tp1의 값 : ", tp1)
print("tp1의 요소값이 85인 개수 : ", tp1.count(85))
print("tp1의 요소의 값이 75인 요소의 인덱스 : ", tp1.index(75))