# 연산자 : 데이터를 가공하거나 비교하거나 판단
# 산술연산자 : +, -, *, /, %(나머지), //(몫), **(거듭제곱)
# 관계연산자 : ==, !=, >(초과), <(미만), >=(이상,이후), <=(이하,이전)
# 논리연산자 : and, or, not
# 대입연산자 : =, (a, b), *(패키징)

a = 10
b = 8
print("더하기 : ", a+b)
print("빼기 : ", a-b)
print("곱하기 : ", a*b)
print("나누기 : ", a/b)
print("나머지 : ", a%b)
print("몫 : ", a//b)
print("거듭제곱 : ", a**b)